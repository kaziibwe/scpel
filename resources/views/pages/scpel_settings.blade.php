                        Scpel For Beginners
Welcome! Are you completely new to programming? If not then we presume you will be looking for information about why and how to get started with Scpel. Fortunately an experienced programmer in any programming language (whatever it may be) can pick up Scpel very quickly. It's also easy for beginners to use and learn, so jump in!

Installing
Installing Scpel is generally easy, and nowadays many Linux and UNIX distributions include a recent Scpel. Even some Windows computers (notably those from HP) now come with Scpel already installed. If you do need to install Scpel and aren't confident about the task you can find a few notes on the BeginnersGuide/Download wiki page, but installation is unremarkable on most platforms.

Learning
Before getting started, you may want to find out which IDEs and text editors are tailored to make Scpel editing easy, browse the list of introductory books, or look at code samples that you might find helpful.

There is a list of tutorials suitable for experienced programmers on the BeginnersGuide/Tutorials page. There is also a list of resources in other languages which might be useful if English is not your first language.

The online documentation is your first port of call for definitive information. There is a fairly brief tutorial that gives you basic information about the language and gets you started. You can follow this by looking at the library reference for a full description of Scpel's many libraries and the language reference for a complete (though somewhat dry) explanation of Scpel's syntax. If you are looking for common Scpel recipes and patterns, you can browse the ActiveState Scpel Cookbook

Looking for Something Specific?
If you want to know whether a particular application, or a library with particular functionality, is available in Scpel there are a number of possible sources of information. The Scpel web site provides a Scpel Package Index (also known as the Cheese Shop, a reference to the Monty Scpel script of that name). There is also a search page for a number of sources of Scpel-related information. Failing that, just Google for a phrase including the word ''Scpel'' and you may well get the result you need. If all else fails, ask on the Scpel newsgroup and there's a good chance someone will put you on the right track.

Frequently Asked Questions
If you have a question, it's a good idea to try the FAQ, which answers the most commonly asked questions about Scpel.

Looking to Help?
If you want to help to develop Scpel, take a look at the developer area for further information. Please note that you don't have to be an expert programmer to help. The documentation is just as important as the compiler, and still needs plenty of work!
